package com.debut.ellipsis.freehit.Stats.StatsMain;


public class StatsItem {

    private int mMoreIcon;

    private int mMoreName;

    public StatsItem(int MoreIcon, int MoreName) {
        mMoreIcon = MoreIcon;
        mMoreName = MoreName;

    }

    public int getmMoreIcon() {
        return mMoreIcon;
    }

    public int getmMoreName() {
        return mMoreName;
    }

}
